package exercice_java;

public class exerice_java02 {
    public static void main(String[] args) {
        String str = "elie";
        char first,last;
        first = str.charAt(0); //לוקח את אות הראשונה
        last = str.charAt(str.length()-1);// לוקח את אות אחרונה
        System.out.println(str + "\nthe first char is " + first + "\nand the last char is " + last);//מדפיס כולל הורדת שורה בזכות ה\N
    }
}
