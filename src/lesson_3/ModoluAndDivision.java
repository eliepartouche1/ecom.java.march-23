package lesson_3;

public class ModoluAndDivision {
    public static void main(String[] args) {
        int a = 3687;


        System.out.println(a + "%" + 10 + "=" + (a % 10));
        System.out.println(a + "%" + 100 + "=" + (a % 100));
        System.out.println(a + "/" + 10 + "=" + (a / 10));
        System.out.println(a + "/" + 100 + "=" + (a / 100));









    }










}
